package iitu.edu.kz.AkseleuMaksat.DTO;

import lombok.Data;

@Data

public class NewsTagDTO {

    private Long id;
    private Long newsId;
    private Long tagId;

    // Getters and Setters
    public Long getId() {
        return id;
    }

    public void setId(Long id) {
        this.id = id;
    }

    public Long getNewsId() {
        return newsId;
    }

    public void setNewsId(Long newsId) {
        this.newsId = newsId;
    }

    public Long getTagId() {
        return tagId;
    }

    public void setTagId(Long tagId) {
        this.tagId = tagId;
    }
}